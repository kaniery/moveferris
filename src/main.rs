use std::thread;
use std::time::Duration;

fn main() {
    let ferris = r#"                                                  
                  - .== =+:.=-  :                 
              .- =++++++++++++++= :+              
    .       ::-+++++++++++++++++++++:::     :-  . 
 =- -+=     ++++++++++++++++++++++++++-   -++- :+ 
 ++-=++= .=+++++++++++++++++++++++++++++- -+++++- 
  -++++ ===+++++++++++++++++++++++++++++==:.++-   
    :++.:+++++++++++- +@#++*- %%++++++++++=+-     
      .+++++++++++++*@@@%++*@@@@+++++++++++-      
      ++--**++++++++++++++++++++++++++*++*++=     
       -+-.*  .----*******#*****+---   :-.=:      
         :+                             .=.       
           :.                           : 
    "#;

    for i in 0..20 {
        // 1. 画面をクリア（または改行で押し上げる）
        print!("{}[2J{}[H", 27 as char, 27 as char);

        // 2. 左側にスペースを入れて位置をずらす
        let indent = " ".repeat(i);
        for line in ferris.lines() {
            println!("{}{}", indent, line);
        }

        // 3. 少し待機（秒間10フレーム程度）
        thread::sleep(Duration::from_millis(100));
    }
}